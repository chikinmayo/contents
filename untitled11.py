def is_foreign_language(text):
    """리뷰 텍스트가 스페인어 등 비영어/비한국어인지 판별"""
    if not text or not isinstance(text, str) or text.strip() == "":
        return False
    # 한글이 포함되어 있는지 확인 -> 한국어 리뷰는 번역에서 제외
    has_korean = bool(re.search(r'[\uac00-\ud7a3]', text))
    if has_korean:
        return False

    # 특수 액센트 문자 확인 (스페인어, 프랑스어, 독일어 등)
    has_foreign_char = bool(re.search(r'[áéíóúñ¿¡çàèùâêîôûëïüöäßßæœÁÉÍÓÚÑÇÀÈÙÂÊÎÔÛËÏÜÖÄ]', text))
    if has_foreign_char:
        return True

    # 유럽계 다빈도 단어 확인 (스페인어, 프랑스어, 독일어 등)
    foreign_keywords = r'\b(el|la|los|las|de|en|que|y|un|una|es|muy|con|excelente|bueno|malo|pelicula|serie|le|les|des|du|et|une|est|pour|dans|avec|der|die|das|und|ist|in|mit|zu|von|se|non|si|un|una|pero)\b'
    if re.search(foreign_keywords, text, re.IGNORECASE):
        return True

    return False

def translate_to_english(text):
    """비한국어/비영어 텍스트를 실시간으로 영어로 번역 (Google Translate + MyMemory 이중화 및 캐싱)"""
    if not text or not isinstance(text, str) or text.strip() == "":
        return ""

    # 캐시 확인
    if text in translation_cache:
        return translation_cache[text]

    clean_text = text.replace('\n', ' ').replace('\r', ' ').strip()

    # 1단계: Google Translate gtx 무료 API 시도 (가장 빠르고 한도 제한 거의 없음)
    try:
        url = "https://translate.googleapis.com/translate_a/single"
        params = {
            "client": "gtx",
            "sl": "auto",
            "tl": "en",
            "dt": "t",
            "q": clean_text[:1000]
        }
        headers = {
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/100.0.0.0 Safari/537.36"
        }
        response = requests.get(url, params=params, headers=headers, timeout=5)
        if response.status_code == 200:
            res_json = response.json()
            translated_sentences = []
            if res_json and len(res_json) > 0 and res_json[0]:
                for part in res_json[0]:
                    if part and len(part) > 0:
                        translated_sentences.append(part[0])
            if translated_sentences:
                translated = "".join(translated_sentences)
                translation_cache[text] = translated
                save_cache()
                return translated
    except Exception as e:
        # Google Translate 실패 시 MyMemory API로 자동 백업 이중화 우회
        pass

    # 2단계: MyMemory 번역 API 우회 시도
    try:
        url = f"https://api.mymemory.translated.net/get?q={requests.utils.quote(clean_text[:500])}&langpair=AUTO|en"
        response = requests.get(url, timeout=5)
        if response.status_code == 200:
            data = response.json()
            if 'responseData' in data and 'translatedText' in data['responseData']:
                translated = data['responseData']['translatedText']
                translation_cache[text] = translated
                save_cache()
                return translated
        elif response.status_code == 429:
            print(f"\n  ⚠️ [번역 트래픽 초과] MyMemory API 일일 한도가 초과되었습니다.")
    except Exception as e:
        print(f"\n  ⚠️ [번역 통신 에러] 번역 API 호출 중 예외 발생: {e}")
        print("  -> 인터넷 접속 상태 혹은 방화벽/샌드박스 네트워크 차단 설정을 점검해 주세요.")

    return text  # 모든 API 실패 시 원본 유지

def clean_korean_word(word):
    """한국어 조사 정밀 제거 필터"""
    if len(word) <= 1:
        return ""
    # 은/는/이/가/을/를/의/에/로/도 등 조사 제거 정규식
    word = re.sub(r'(은|는|이|가|을|를|의|에|로|으로|에서|에게|한테|과|와|도|만|고|며|이네|이고|이지만|이며|이다|하고|해서|보다|처럼|조차|마저|요|입니다|합니다|였다|했다|한다)$', '', word)
    if len(word) <= 1 or word in korean_stop_words:
        return ""
    return word

def clean_english_word(word):
    """영어 관사 및 불용어 제거 필터"""
    word = re.sub(r'[^a-z0-9]', '', word.lower())
    if len(word) <= 2 or word in english_stop_words:
        return ""
    return word

def extract_keywords(text):
    """텍스트에서 정제된 단어들을 주입하고 카운팅하는 형태소 분리기"""
    if not text or not isinstance(text, str):
        return []

    # 특수기호 거르고 띄어쓰기로 토큰 분리
    words = re.sub(r'[.,\/#!$%\^&\*;:{}=\-_`~()?]', '', text).split()
    cleaned_words = []

    for w in words:
        if len(w.strip()) <= 1:
            continue
        has_korean = bool(re.search(r'[\uac00-\ud7a3]', w))
        if has_korean:
            cleaned = clean_korean_word(w)
        else:
            cleaned = clean_english_word(w)

        if cleaned:
            cleaned_words.append(cleaned)

    return cleaned_words

