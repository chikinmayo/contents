def find_optimal_columns(df, filepath):
    """각 파일마다 제목, 평점, 리뷰내용에 부합하는 열 이름을 지능적으로 자동 탐색"""
    cols = df.columns.tolist()
    title_col, rating_col, review_col = None, None, None

    title_kws = ['제목', '작품', '프로그램', '콘텐츠', 'title', 'show', 'name', 'movie', 'series']
    rating_kws = ['별점', '평점', '점수', 'rating', 'score', 'star', '평가']
    review_kws = ['리뷰', '내용', '코멘트', '댓글', '텍스트', 'review', 'text', 'comment', 'content']

    # 1단계: 완전 매칭 또는 포함 매칭 탐색
    for col in cols:
        col_lower = str(col).lower()
        if any(kw in col_lower for kw in title_kws) and not title_col:
            title_col = col
        if any(kw in col_lower for kw in rating_kws) and not rating_col:
            rating_col = col
        if any(kw in col_lower for kw in review_kws) and not review_col:
            review_col = col

    # 2단계: 매핑 차선책 설정 (작품명이 없을 시 파일 이름을 작품명으로 스마트 승계)
    if not title_col:
        title_col = "auto_extracted_filename"
    if not rating_col:
        # 숫자 타입 데이터가 대다수 포함된 열로 강제 매핑 시도
        for col in cols:
            if df[col].dtype in ['int64', 'float64']:
                rating_col = col
                break
    if not review_col:
        # 가장 텍스트 길이가 긴 열로 강제 매핑 시도
        longest_avg = 0
        for col in cols:
            if df[col].dtype == 'object':
                avg_len = df[col].astype(str).str.len().mean()
                if avg_len > longest_avg:
                    longest_avg = avg_len
                    review_col = col

    return title_col, rating_col, review_col

